/* ---- Core functions ---- */

function applyLang(lang) {
  // First pass: save originals
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    if (el.dataset.i18nOriginal === undefined) {
      el.dataset.i18nOriginal = el.innerHTML;
    }
  });

  // Apply / restore translations
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.dataset.i18n;
    if (lang === 'ru' && TRANSLATIONS.ru[key] !== undefined) {
      el.innerHTML = TRANSLATIONS.ru[key];
    } else {
      el.innerHTML = el.dataset.i18nOriginal;
    }
  });

  // Page title
  var page = document.body.dataset.page;
  if (!document.body.dataset.titleOriginal) {
    document.body.dataset.titleOriginal = document.title;
  }
  if (lang === 'ru' && TRANSLATIONS.ru['title.' + page]) {
    document.title = TRANSLATIONS.ru['title.' + page];
  } else {
    document.title = document.body.dataset.titleOriginal;
  }

  // HTML lang attribute
  document.documentElement.lang = lang;

  // Switcher active state
  document.querySelectorAll('[data-lang-btn]').forEach(function(a) {
    a.classList.toggle('lang-active', a.dataset.langBtn === lang);
  });
}

/* ---- Init ---- */

document.addEventListener('DOMContentLoaded', function() {
  // Attach click handlers
  document.querySelectorAll('[data-lang-btn]').forEach(function(a) {
    a.addEventListener('click', function(e) {
      e.preventDefault();
      var lang = a.dataset.langBtn;
      localStorage.setItem('lang', lang);
      applyLang(lang);
    });
  });

  // Apply stored or default language
  var lang = localStorage.getItem('lang') || 'en';
  applyLang(lang);
});
